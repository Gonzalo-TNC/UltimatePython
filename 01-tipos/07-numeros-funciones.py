import math

print(round(1.3))  # Devuelve el numero entero más cercano
print(round(1.7))
print(round(1.5))
print(abs(-77))  # Entrega el valor absoluto de un número
print(abs(55))

print(math.ceil(1.1))  # Redondea un numero hacia arriba
print(math.floor(1.99999))  # redonda un numero hacia abajo
print(math.isnan(23))  # Devuelve "True" si el valor NO ES UN NUNMERO
print(math.pow(10, 3))  # Eleva el primer número al segundo (potencia)
print(math.sqrt(9))  # Devuelve la raiz cuadrada de un número
